package awadhh;


public class School {
	private int studNum;
	private String studName;

	public int getStudNum() {
		return studNum;
	}

	public void setStudNum(int studNum) {
		this.studNum = studNum;
	}

	public String getStudName() {
		return studName;
	}

	public void setStudName(String studName) {
		this.studName = studName;
	}

}
